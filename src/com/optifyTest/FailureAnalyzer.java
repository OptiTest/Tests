    private void creatTable(){
        final int ROW=failures.getOldFailureList().size();
        final int COL=6;
        Object object[][]=new Object[ROW][COL];
        jScrollPane1.setBackground(Color.white);
        jScrollPane1.setBorder(javax.swing.BorderFactory.createEmptyBorder(0, 0, 0, 0));
        
        int r=0;  //Row index.
        for(NodeFailure i:this.failures.getOldFailureList()){
				int c=0; //Col index.
        		
				object[r][c]=null;c++;
				object[r][c]=parsePageName(i.returnPageName());c++;
				object[r][c]=i.returnObjectName();c++;
				object[r][c]=i.returnActionPerformed();c++;
				object[r][c]=i.returnErrorDescription();c++;
				object[r][c]=i.getNum();c++;r++;
		}
        
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
        	object,
            new String [] {
                "Select", "Page name", "Object name", "Action performed", "Error description", "Instances"
            }
        ) {
            @SuppressWarnings("rawtypes")
			Class[] types = new Class [] {
                java.lang.Boolean.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.Integer.class
            };

            @SuppressWarnings("rawtypes")
			public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }
        });
        
        //Set column with:
        jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_OFF);
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(45);
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(120);
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(180);
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(180);
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(190);
        jTable1.getColumnModel().getColumn(5).setPreferredWidth(45);
    }
    
    private String parsePageName(String str){
    	String arr[]=str.split("com.optifyTest.");
    	
    	return arr[1];
    }
    
    private void deleteFailure(){
    	int selected=JOptionPane.showConfirmDialog(null,"Are you sure?","Delete choosen failures?",JOptionPane.YES_NO_OPTION);
    	
    	int j=0;
    	int SIZE=failures.getOldFailureList().size();
    	if(selected==JOptionPane.YES_OPTION){
	    	for(int i=0; i<SIZE;i++){
	    		if(jTable1.getModel().getValueAt(i, 0)!=null&&((Boolean)jTable1.getModel().getValueAt(i, 0))){
	    			failures.getOldFailureList().remove(i-j);
	    			j++;
	    		}
	    	}
	    	
	    	failures.saveToLog();
    	}
    	
    	creatTable();
    }
    
