    private void jButSelDesActionPerformed(java.awt.event.ActionEvent evt){
    	final int SIZE=failures.getOldFailureList().size();
    	
    	
    	for(int i=0; i<SIZE;i++){
    		if(jTable1.getModel().getValueAt(i, 0)!=null&&((Boolean)jTable1.getModel().getValueAt(i, 0)));
    			
    		else{
    			for(int j=0; j<SIZE;j++)
    				jTable1.getModel().setValueAt(true, j, 0);
    			
    			return;
    		}	
    	}
    	
    	for(int j=0; j<SIZE;j++)
			jTable1.getModel().setValueAt(false, j, 0);
    }
    
   
